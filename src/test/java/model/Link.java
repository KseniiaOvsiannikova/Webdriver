package model;

public class Link {

    private String GOOGLE_URL;
    private String WIKIPEDIA_URL;
    private String JQUERY_URL;
    private String TUTORIALSPOINT_URL;

    public Link(String GOOGLE_URL, String WIKIPEDIA_URL, String JQUERY_URL, String TUTORIALSPOINT_URL) {
        this.GOOGLE_URL = GOOGLE_URL;
        this.WIKIPEDIA_URL = WIKIPEDIA_URL;
        this.JQUERY_URL = JQUERY_URL;
        this.TUTORIALSPOINT_URL = TUTORIALSPOINT_URL;
    }
}
